class Typecasting
{
 public static void main(String args[])
{
 byte b=12;
int i=b;
double d=123.45;
float f=(float)d;
char c='v';
int in=(int)c;
System.out.println("The integer value is:"+i);
System.out.println("The float value is:"+f);
System.out.println("The ascii value is:"+in);

 }
}
 